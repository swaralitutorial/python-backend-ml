# Float Practice

a = 10.5
b = 2.3

print("Value of a:", a)
print("Value of b:", b)

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)

# Float precision example
x = 0.1
y = 0.2

print("Normal Result:", x + y)
print("Rounded Result:", round(x + y, 2))
